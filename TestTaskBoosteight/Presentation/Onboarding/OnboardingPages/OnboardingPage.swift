import Foundation

/// Простая модель данных для одного экрана онбординга.
/// Конформится к Equatable для сравнения и поиска индекса.
/// Используется в контроллере/вью-модели онбординга.
struct OnboardingPage: Equatable {
    /// Это заголовок экрана и как он отображается.
    let title: String
    /// Это подзаголовок/описание, возможные переносы строк.
    let subtitle: String
    /// Это имена ассетов изображений, порядок важен, и как добавлять новые ассеты в проект.
    let images: [String]
}
